package org.foo.modules.dummycsrftest.actions;

import org.jahia.bin.Action;
import org.jahia.bin.ActionResult;
import org.jahia.services.content.JCRNodeWrapper;
import org.jahia.services.content.JCRSessionWrapper;
import org.jahia.services.render.RenderContext;
import org.jahia.services.render.Resource;
import org.jahia.services.render.URLResolver;
import org.osgi.service.component.annotations.Activate;
import org.osgi.service.component.annotations.Component;
import org.slf4j.Logger;
import org.slf4j.LoggerFactory;

import javax.servlet.http.HttpServletRequest;
import java.util.List;
import java.util.Map;

/**
 * Short description of the class
 *
 * @author yousria
 */
@Component(service = Action.class)
public class UpdateTextAction extends Action {

    private static final Logger LOGGER = LoggerFactory.getLogger(UpdateTextAction.class.getName());

    @Activate
    public void activate() {
        setName("modifyTextAction");
        setRequireAuthenticatedUser(false);
        setRequiredMethods("GET,POST");
    }

    @Override
    public ActionResult doExecute(HttpServletRequest req, RenderContext renderContext, Resource resource,
            JCRSessionWrapper session, Map<String, List<String>> parameters, URLResolver urlResolver) throws Exception {
        JCRNodeWrapper node = resource.getNode();
        if (node.isNodeType("jnt:testContent")) {
            node.setProperty("text", req.getParameter("newText"));
            session.save();
            return ActionResult.OK;
        }
        return null;
    }
}
